/// عنصر ذكر واحد: نصه، عدد مرات تكراره، وملف الصوت الذي سيُضاف لاحقًا.
class AdhkarItem {
  final String id;
  final String text;
  final int repeat;
  final String? note;

  const AdhkarItem({
    required this.id,
    required this.text,
    this.repeat = 1,
    this.note,
  });

  /// مسار الصوت المتوقّع لهذا الذكر ضمن مجلد assets. المستخدم سيسجّل
  /// ملفات mp3 بهذه الأسماء بالضبط ويضعها في المجلد المناسب لاحقًا.
  String audioAsset(String category) => 'audio/$category/$id.mp3';
}

